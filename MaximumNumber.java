
public class MaximumNumber {

	public static void main(String[] args) {
		int a=8;
		int b=5;
		int c=3;
		
		if(a>b && a>c)
		{
			System.out.println(a+" is a max number");
		}
		else if(b>a && b>c)
		{
			System.out.println(b+" is a max number");
		}
		else
		{
			System.out.println(c+" is a max number");
		}
	}

}
